package kr.co.collie.user.item.domain;

public class ItemQnaDomain {
	int idx, item_qna_num;
	String id, item_qna_subject, item_qna_flag, input_date;
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getItem_qna_num() {
		return item_qna_num;
	}
	public void setItem_qna_num(int item_qna_num) {
		this.item_qna_num = item_qna_num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getItem_qna_subject() {
		return item_qna_subject;
	}
	public void setItem_qna_subject(String item_qna_subject) {
		this.item_qna_subject = item_qna_subject;
	}
	public String getItem_qna_flag() {
		return item_qna_flag;
	}
	public void setItem_qna_flag(String item_qna_flag) {
		this.item_qna_flag = item_qna_flag;
	}
	public String getInput_date() {
		return input_date;
	}
	public void setInput_date(String input_date) {
		this.input_date = input_date;
	}
	
}//class
